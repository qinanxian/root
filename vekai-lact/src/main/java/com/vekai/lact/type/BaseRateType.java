package com.vekai.lact.type;

/**
 * 利率利率类型
 */
public enum BaseRateType {
    /**
     * 央行基准贷款利率(People's Bank of China Loan benchmark Interest rate)
     */
    PBOCLoan,
    /**
     * Shanghai InterBank Offered Rate,上海银行间同业拆放利率;
     */
    SHIBOR,
    /**
     * London InterBank Offered Rate,伦敦同业拆借利率
     */
    LIBOR,
}
